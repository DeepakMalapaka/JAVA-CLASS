class Integer_Literals
{
    public static void main(String[] args) 
    {
      int a=10;
      int b=010;
      int c=0x10;
      int d=0B001;
      System.out.println("Decimal : "+a);
      System.out.println("Octal : "+b);
      System.out.println("Hexadecimal : "+c);
      System.out.println("Binary : "+d);

    }
}
